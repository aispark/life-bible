export const parseSearchParagraph = item => {
  const category = item.substring(0, item.indexOf("(")).trim();
  const [chapter, paragraph] = item
    .substring(item.indexOf("(") + 1, item.indexOf(")"))
    .trim()
    .split(":");

  return [category, chapter, paragraph];
};

export const getFullCategory = item => {
  const category = item.substring(0, item.indexOf("(")).trim();
  const chapter = item
    .substring(item.indexOf("("), item.indexOf(")") + 1)
    .trim();

  const [shortNm, fullNm] = dictionary.find(
    ([shortNm]) => shortNm === category
  );
  return fullNm + chapter;
};

